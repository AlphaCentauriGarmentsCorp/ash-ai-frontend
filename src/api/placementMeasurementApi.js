import api from "./axios";

export const placementMeasurementApi = {
  create: async (payload) => {
    const { data } = await api.post("/placement-measurement", payload, {
      headers: { "Content-Type": "multipart/form-data" },
    });
    return data;
  },

  index: async (params = {}) => {
    try {
      const response = await api.get("/placement-measurement", { params });
      return response.data;
    } catch (error) {
      throw error;
    }
  },

  show: async (id) => {
    try {
      const response = await api.get(`/placement-measurement/${id}`);
      return response.data;
    } catch (error) {
      throw error;
    }
  },

  update: async (id, payload) => {
    try {
      const response = await api.put(`/placement-measurement/${id}`, payload);
      return response.data;
    } catch (error) {
      throw error;
    }
  },

  delete: async (id) => {
    try {
      const response = await api.delete(`/placement-measurement/${id}`);
      return response.data;
    } catch (error) {
      throw error;
    }
  },
};
